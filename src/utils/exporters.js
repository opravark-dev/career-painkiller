// Dynamic import keeps jsPDF out of the main bundle.
export async function downloadResumePdf(resumeText, filename = 'Optimised_Resume.pdf') {
  const { jsPDF } = await import('jspdf');
  const doc = new jsPDF({ unit: 'pt', format: 'letter' });
  drawResumeToPdf(doc, resumeText);
  doc.save(filename);
}

